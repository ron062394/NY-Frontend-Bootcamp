//Looping Statements - Execute repetitive task / way to automate task

//Loops: for loop
let e = 1;
for (let i = e; i <= 5; i++ ) {
    console.log("For Count:", i);
}


//Loops: while loop
let count = 1;
while (count <= 5) {
    count++;
    console.log("While Count:", count);
}


//Loops: Do-while loop
let counter = 6;
do {
    console.log("Do While Count:", counter);
    counter++;
} while (counter <= 5);