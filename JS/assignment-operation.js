//Assignment operation
let a = 10;
let b = 5;

// '+=' - addition
a += b;
console.log("The value of a is", a);

// '-=' - substraction
a -= b;
console.log("The value of a is", a);

// '*=' - multiplication
a *= b;
console.log("The value of a is", a);

// '/=' - division
a /= b;
console.log("The value of a is", a);

