//JS Operators
let x = 5;
let y = 2;

//Arithmetic operators

//Addition '+' operator
let sum = x + y;
console.log("The value of sum is", sum);

//Substraction '-' operator
let difference = x - y;
console.log("The value of difference is", difference);

//Multiplication '*' operator
let multiplication = x * y;
console.log("The value of multiplication is", multiplication);

//Division '-' operator
let division = x / y;
console.log("The value of division is", division);